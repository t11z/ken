//! Typed identifier newtypes for the Ken protocol.
//!
//! Each identifier is a newtype around [`Uuid`] that prevents mix-ups
//! at the type level. An [`EndpointId`] cannot be passed where a
//! [`CommandId`] is expected, even though both are UUIDs internally.
//! See ADR-0001 for the trust boundaries these identifiers enforce.

use std::fmt;

use serde::{Deserialize, Serialize};
use uuid::Uuid;

/// Stable identifier for an enrolled endpoint, assigned by the server
/// at enrollment time.
///
/// Endpoints are enrolled manually by the family IT chief per
/// ADR-0001 T2-7. This type is the handle to a single enrolled agent
/// throughout the system.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct EndpointId(Uuid);

impl EndpointId {
    /// Generate a fresh random endpoint identifier.
    #[must_use]
    pub fn new() -> Self {
        Self(Uuid::new_v4())
    }

    /// Create an identifier from an existing UUID.
    #[must_use]
    pub fn from_uuid(id: Uuid) -> Self {
        Self(id)
    }

    /// Parse an identifier from its string representation.
    ///
    /// # Errors
    ///
    /// Returns an error if the string is not a valid UUID.
    pub fn parse(s: &str) -> Result<Self, uuid::Error> {
        Uuid::parse_str(s).map(Self)
    }

    /// Return the underlying UUID.
    #[must_use]
    pub fn as_uuid(&self) -> &Uuid {
        &self.0
    }
}

impl Default for EndpointId {
    fn default() -> Self {
        Self::new()
    }
}

impl fmt::Display for EndpointId {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.0)
    }
}

