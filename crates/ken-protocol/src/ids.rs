#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn endpoint_id_display_matches_uuid() {
        let uuid = Uuid::new_v4();
        let id = EndpointId::from_uuid(uuid);
        assert_eq!(id.to_string(), uuid.to_string());
    }

    #[test]
    fn command_id_display_matches_uuid() {
        let uuid = Uuid::new_v4();
        let id = CommandId::from_uuid(uuid);
        assert_eq!(id.to_string(), uuid.to_string());
    }

    #[test]
    fn heartbeat_id_display_matches_uuid() {
        let uuid = Uuid::new_v4();
        let id = HeartbeatId::from_uuid(uuid);
        assert_eq!(id.to_string(), uuid.to_string());
    }

    #[test]
    fn session_id_display_matches_uuid() {
        let uuid = Uuid::new_v4();
        let id = SessionId::from_uuid(uuid);
        assert_eq!(id.to_string(), uuid.to_string());
    }

    #[test]
    fn endpoint_id_parse_roundtrip() {
        let id = EndpointId::new();
        let parsed = EndpointId::parse(&id.to_string()).unwrap();
        assert_eq!(id, parsed);
    }

    #[test]
    fn different_id_types_are_not_interchangeable() {
        // This test exists to document the type-safety guarantee.
        // The compiler prevents passing a CommandId where an EndpointId
        // is expected — this test verifies the types are distinct at
        // the value level too.
        let uuid = Uuid::new_v4();
        let endpoint = EndpointId::from_uuid(uuid);
        let command = CommandId::from_uuid(uuid);
        // Same underlying UUID, but different types — cannot be compared.
        assert_eq!(endpoint.to_string(), command.to_string());
    }
}
